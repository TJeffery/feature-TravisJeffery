    public function store(Request $request)
    {

        $attrubutes = $request->validate([
            'name'=> 'required|min:3|max:255',
            'Description'=>'required|min:10|max:255',
            'UserID'=>'required'
        ]);
        //dd($attrubutes);
        Post::create($attrubutes);
